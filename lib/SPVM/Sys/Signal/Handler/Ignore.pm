=head1 Name

SPVM::Sys::Signal::Handler::Ignore - Signal Handler that Represents C<SIG_IGN> in C<C language>

=head1 Usage

  use Sys::Signal::Handler::Ignore;

=head1 Description

C<Sys::Signal::Handler::Ignore> is a signal handler that represents C<SIG_IGN> in C<C language>.

See L<signal(2) - Linux man page|https://linux.die.net/man/2/signal> in Linux.

See L<Sys::Signal|SPVM::Sys::Signal> about the details of signals.
