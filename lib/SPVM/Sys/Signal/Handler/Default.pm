=head1 Name

SPVM::Sys::Signal::Handler::Default - Signal Handler that Represents C<SIG_DFL> in C<C language>.

=head1 Usage

  use Sys::Signal::Handler::Default;

=head1 Description

C<Sys::Signal::Handler::Default> is a signal handler that represents C<SIG_DFL> in C<C language>.

See L<signal(2) - Linux man page|https://linux.die.net/man/2/signal> in Linux.

See L<Sys::Signal|SPVM::Sys::Signal> about the details of signals.

